import torch

# =============================================================================
# PyTorch 自动求导（Autograd）基础
# =============================================================================
print("=" * 60)
print("PyTorch 自动求导（Autograd）基础")
print("=" * 60)
# PyTorch 的 autograd（自动求导）机制可以自动计算梯度
# 只需要对需要求导的张量设置 requires_grad=True，然后进行前向运算，
# 最后调用 .backward() 即可自动计算梯度

# 创建张量，默认为浮点类型
x = torch.arange(4.0)
print(f"x = {x}")

# =============================================================================
# 开启梯度追踪
# =============================================================================
print("\n" + "=" * 60)
print("开启梯度追踪")
print("=" * 60)
# requires_grad=True 开启该张量的梯度追踪
# 开启后，所有对该张量的操作都会被记录，用于后续的梯度计算
x.requires_grad_(True)
print(f"x.grad (求导前) = {x.grad}")  # None，尚未进行反向传播

# =============================================================================
# 构造计算图并计算梯度
# =============================================================================
print("\n" + "=" * 60)
print("构造计算图并计算梯度")
print("=" * 60)
# y = 2 * torch.dot(x, x) = 2 * x·x = 2 * (0^2 + 1^2 + 2^2 + 3^2) = 2 * 14 = 28
# torch.dot(x, x) 计算向量x与自身的点积，即 x·x = sum(x_i^2)
y = 2 * torch.dot(x, x)
print(f"y = 2 * torch.dot(x, x) = {y}")

# =============================================================================
# 反向传播计算梯度
# =============================================================================
print("\n" + "=" * 60)
print("反向传播计算梯度")
print("=" * 60)
# y.backward() 进行反向传播，计算所有梯度
# 梯度会自动存储在 x.grad 中
# y = 2*x·x，对x求导：dy/dx = 4x
y.backward()
print(f"x.grad (y.backward()后) = {x.grad}")
print(f"x.grad == 4 * x : {x.grad == 4 * x}")

# =============================================================================
# 清除旧梯度
# =============================================================================
print("\n" + "=" * 60)
print("清除旧梯度")
print("=" * 60)
# 同一张量多次反向传播时，需要先清除旧梯度
# 否则梯度会累加（accumulate）
x.grad.zero_()
print(f"x.grad.zero_() 后 x.grad = {x.grad}")

# =============================================================================
# 新的前向传播与反向传播
# =============================================================================
print("\n" + "=" * 60)
print("新的前向传播与反向传播")
print("=" * 60)
# y = x.sum() = 0 + 1 + 2 + 3 = 6
# 对x求导：dy/dx_i = 1（每个元素的偏导都是1）
y = x.sum()
print(f"y = x.sum() = {y}")
y.backward()
print(f"x.grad (y.backward()后) = {x.grad}")  # 全是1

# =============================================================================
# 复杂计算图：y = x * x
# =============================================================================
print("=" * 60)
print("复杂计算图：y = x * x")
print("=" * 60)
# 先清除旧梯度
x.grad.zero_()
# y = x * x 是逐元素乘法，y_i = x_i * x_i = x_i^2
# y.sum() = sum(x_i^2)，对x求导：dy/dx_i = 2*x_i
y = x * x
print(f"y = x * x = {y}")
y.sum().backward()
print(f"x.grad (y.sum().backward()后) = {x.grad}")  # 2*x

# =============================================================================
# detach()：分离张量，切断梯度追踪
# =============================================================================
print("=" * 60)
print("detach()：分离张量，切断梯度追踪")
print("=" * 60)
# detach() 将张量从计算图中分离出来，后续操作不再追踪梯度
# 常用于需要保持张量值但不想影响梯度的场景

x.grad.zero_()
y = x * x                # y = x^2，仍在计算图中
u = y.detach()           # u 脱离计算图，不再追踪梯度
z = u * x                # z = u * x，但u不会产生梯度
print(f"y = x * x = {y}")
print(f"u = y.detach() = {u}")
print(f"z = u * x = {z}")

# z.sum().backward() 前清除梯度
x.grad.zero_()
print(f"x.grad.zero_() 后 x.grad = {x.grad}")

# 反向传播：z = u*x，u为常数，dz/dx = u
z.sum().backward()
print(f"z.sum().backward() 后 x.grad = {x.grad}")  # u 的值（因为 dz/dx = u）
print(f"x.grad == u : {x.grad == u}")  # x.grad 等于 u，因为 dz/dx = u

# =============================================================================
# 对比：直接对 y 求导
# =============================================================================
print("\n" + "=" * 60)
print("对比：直接对 y 求导")
print("=" * 60)
x.grad.zero_()
print(f"x.grad.zero_() 后 x.grad = {x.grad}")
y.sum().backward()
print(f"y.sum().backward() 后 x.grad = {x.grad}")  # 2*x
print(f"x.grad == 2 * x : {x.grad == 2 * x}")